package MapGenerator3_UI_Friendlier;

import java.awt.Rectangle;

public class Rect extends Rectangle{
	public int x = 0;
	public int y = 0;
	public int width = 0;
	public int length = 0;

	public Rect(int x, int y, int width, int length) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.length = length;
	}
	
}
